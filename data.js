// FUNCTIONS //
function clearContainer(){
	container.innerHTML = " ";
}
function printCard(obj){
	const card = document.createElement("div");
	card.classList.add("card");
	const emoji = document.createElement("div");
	emoji.innerHTML = `<i class="fa-solid ${obj.prefix}${obj.name}"></i>`;
	emoji.style.color = obj.color;
	const emojiName = document.createElement("div");
	emojiName.innerHTML = obj.name;
	card.appendChild(emoji);
	card.appendChild(emojiName);
	container.appendChild(card);
}
function filter(){
	const filter = selection.value;
	if (filter == "all"){
		clearContainer();
		emoji.forEach(printCard);
	} else {
		const filtered = emoji.filter(emoji => emoji.type == filter);
		clearContainer();
		filtered.forEach(printCard);
	}
}
function rainbowPossession(){
	emoji.forEach((element) => {
		element.color = generateRandomColor();
	})
	clearContainer();
	filter();
}
function generateRandomColor(){
    let maxVal = 0xFFFFFF; // 16777215
    let randomNumber = Math.random() * maxVal; 
    randomNumber = Math.floor(randomNumber);
    randomNumber = randomNumber.toString(16);
    let randColor = randomNumber.padStart(6, 0);   
    return `#${randColor.toUpperCase()}`
}
